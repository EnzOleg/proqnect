from openai import OpenAI

client = OpenAI()

def ask_gpt(prompt):
    system_message = {
        "role": "system",
        "content": (
            "Ты помощник, который помогает пользователю формулировать запрос для эксперта. "
            "Не отвечай на вопрос, не решай задачу — просто переформулируй исходный текст "
            "в виде чёткого и понятного вопроса. Сделай его уважительным и формальным."
        )
    }

    user_message = {"role": "user", "content": prompt}

    response = client.chat.completions.create(
        model="gpt-3.5-turbo",
        messages=[system_message, user_message]
    )

    return response.choices[0].message.content

def ask_gpt_in_help(prompt):
    context = """
    Ты — AI-помощник на сайте платформы для консультаций с экспертами.
    Помогай отвечать на вопросы по проекту. По возможности отвечай кратко. 
    Если вопрос вне темы, вежливо предложи обратиться на почту поддержки help@proqnect.bizml.ru.
    Вот информация о проекте:
    Проект представляет собой онлайн-платформу, предназначенную для поиска экспертов в различных сферах и организации консультаций. 
    Сервис позволяет пользователям находить подходящих специалистов по направлениям, опыту и отзывам, 
    а также записываться на консультации напрямую через страницу эксперта. 
    После отправки запроса эксперт может принять или отклонить запись. В случае подтверждения открывается чат между пользователем и экспертом, 
    где стороны могут согласовать детали встречи.
    На платформе реализована система отзывов и оценок. Оставить отзыв может только тот пользователь, у которого уже была завершённая консультация 
    с соответствующим экспертом. В личном кабинете доступны вкладки с исходящими и входящими заявками на консультации, настройки интерфейса, 
    а также раздел редактирования профиля.
    Пользователь может стать экспертом, подав заявку через специальную форму на платформе. 
    После модерации и подтверждения статус эксперта позволяет принимать заявки, вести консультации и получать отзывы.
    На платформе также реализована лента активности, в которой пользователи могут делиться мыслями, новостями, 
    событиями из жизни и расширять круг общения. Система поддерживает смену темы оформления (светлая/тёмная), загрузку аватара,
    описание профиля и другие элементы персонализации.
    Технически платформа построена на фреймворке Django с использованием базы данных PostgreSQL и 
    контейнеризированного Redis для обеспечения обмена сообщениями в реальном времени. Чат работает на Django Channels и Daphne.
    В процессе реализации находится поддержка видеозвонков с использованием Jitsi и библиотеки lib-jitsi-meet.
    На платформе внедрён умный помощник на базе OpenAI GPT, способный помогать с формулировкой запросов. 
    Также планируется реализовать полноценного AI-консультанта, который будет отвечать на вопросы пользователей о работе платформы. 
    В случае, если вопрос выходит за рамки его знаний, он будет рекомендовать обратиться в службу поддержки по адресу help@proqnect.bizml.ru.
    """

    response = client.chat.completions.create(
        model="gpt-3.5-turbo",
        messages=[
            {"role": "system", "content": context},
            {"role": "user", "content": prompt},
        ]
    )
    return response.choices[0].message.content